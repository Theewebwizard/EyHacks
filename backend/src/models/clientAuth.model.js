import mongoose from "mongoose";

const ClientAuthSchema = mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, { timestamps: true });

const ClientAuth = mongoose.model("ClientAuth", ClientAuthSchema);
export default ClientAuth;
